
public class Usage {
	
	//Declaring instance variable
	private String userName;
	private int count;
	
	//Usage constructor
	public Usage(String userName, int count) {
		
		this.userName = userName;
		this.count = count;
	}
	//getter methods
	public int getCount() {
		return this.count;
	}
	public String getUserName() {
		return this.userName;
	}

}
